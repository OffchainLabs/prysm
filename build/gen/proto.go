var (
	codeGenStart  = "// Code generated by protoc-gen-go"
	protocStampRE = regexp.MustCompile(`^(//[[:space:]]*protoc) +v[0-9].*`)
)

// protoPkgDirs returns the proto package dirs in deterministic (sorted) order.
func protoPkgDirs() []string {
	dirs := make([]string, 0, len(protoPkgs))
	for dir := range protoPkgs {
		dirs = append(dirs, dir)
	}

	sort.Strings(dirs)

	return dirs
}

// genProto regenerates the committed *.pb.go (mainnet-only, untagged) with
// protoc + protoc-gen-go-cast.
func genProto() error {
	protoc, err := exec.LookPath("protoc")
	if err != nil {
		return fmt.Errorf("protoc not found on PATH")
	}

	wktInc := filepath.Clean(filepath.Join(filepath.Dir(protoc), "..", "include"))
	const googleapisInc = "third_party/googleapis"

	tmpRoot, err := os.MkdirTemp("", "gen-proto-")
	if err != nil {
		return fmt.Errorf("mkdirTemp: %w", err)
	}
	defer func() { _ = os.RemoveAll(tmpRoot) }()

	binDir, err := buildProtoPlugins(tmpRoot)
	if err != nil {
		return fmt.Errorf("build proto plugins: %w", err)
	}

	// Stage the whole proto tree with the mainnet dict substituted in.
	stage := filepath.Join(tmpRoot, "stage")
	if err := stageProtos(stage); err != nil {
		return fmt.Errorf("stageProtos: %w", err)
	}
	mmap, err := buildMMAP(stage)
	if err != nil {
		return fmt.Errorf("buildMMAP: %w", err)
	}

	// Comment-free descriptor set over every compiled proto.
	desc := filepath.Join(tmpRoot, "desc.pb")
	var descInputs []string
	for _, dir := range protoPkgDirs() {
		for _, proto := range pkgProtos(dir) {
			descInputs = append(descInputs, filepath.Join(stage, proto))
		}
	}

	descArgs := append([]string{
		"--proto_path=" + stage, "--proto_path=" + googleapisInc, "--proto_path=" + wktInc,
		"--include_imports", "--descriptor_set_out=" + desc,
	}, descInputs...)

	if err := sh(protoc, descArgs...); err != nil {
		return fmt.Errorf("sh: %w", err)
	}

	outDir := filepath.Join(tmpRoot, "out")
	if err := os.MkdirAll(outDir, 0o750); err != nil {
		return fmt.Errorf("mkdir all: %w", err)
	}

	for _, dir := range protoPkgDirs() {
		mode := protoPkgs[dir]
		protos := pkgProtos(dir)
		fmt.Printf("  %s (%s): %d files\n", dir, mode, len(protos))
		opt := "paths=source_relative" + mmap
		pluginFlag, outFlag, optArg, err := protocPluginFlags(mode, binDir, outDir, opt)
		if err != nil {
			return fmt.Errorf("protocPluginFlags: %w", err)
		}

		args := append([]string{"--descriptor_set_in=" + desc, pluginFlag, outFlag, optArg}, protos...)
		if err := sh(protoc, args...); err != nil {
			return fmt.Errorf("sh: %w", err)
		}
	}

	// Strip the leading license block + normalize the non-semantic protoc
	// stamp, then copy each generated file back to its source-relative path.
	err = filepath.WalkDir(outDir, func(path string, d os.DirEntry, err error) error {
		if err != nil || d.IsDir() || !strings.HasSuffix(path, ".pb.go") {
			return err
		}

		if err := normalizeGenerated(path); err != nil {
			return fmt.Errorf("normalizeGenerated: %w", err)
		}

		rel, err := filepath.Rel(outDir, path)
		if err != nil {
			return fmt.Errorf("filepath.Rel: %w", err)
		}

		data, err := os.ReadFile(path) // #nosec G304 -- path comes from WalkDir over our own temp out dir
		if err != nil {
			return fmt.Errorf("readFile: %w", err)
		}

		return os.WriteFile(rel, data, 0o600)
	})
	if err != nil {
		return err
	}

	if err := goimports("proto"); err != nil {
		return fmt.Errorf("goimports: %w", err)
	}

	if err := gofmtSimplify("proto"); err != nil {
		return fmt.Errorf("gofmtSimplify: %w", err)
	}

	return nil
}

// protocPluginFlags returns the protoc plugin, output, and opt args for a
// package compiled in mode.
func protocPluginFlags(mode, binDir, outDir, baseOpt string) (pluginFlag, outFlag, optArg string, err error) {
	castPlugin := "--plugin=protoc-gen-go-cast=" + filepath.Join(binDir, "protoc-gen-go-cast")
	switch mode {
	case modeCast:
		return castPlugin, "--go-cast_out=" + outDir, "--go-cast_opt=" + baseOpt, nil
	case modeCastGRPC:
		return castPlugin, "--go-cast_out=" + outDir, "--go-cast_opt=" + baseOpt + ",plugins=grpc", nil
	case modeStock:
		return "--plugin=protoc-gen-go=" + filepath.Join(binDir, "protoc-gen-go"), "--go_out=" + outDir, "--go_opt=" + baseOpt, nil
	default:
		return "", "", "", fmt.Errorf("unknown proto plugin mode %q", mode)
	}
}

// buildProtoPlugins builds protoc-gen-go-cast and protoc-gen-go against the
// pinned protobuf-go, isolated in a throwaway module so this repo's versions
// don't leak in. Returns the absolute bin directory holding both plugins.
func buildProtoPlugins(tmpRoot string) (string, error) {
	binDir := filepath.Join(tmpRoot, "bin")
	if err := os.MkdirAll(binDir, 0o750); err != nil {
		return "", fmt.Errorf("mkdir all: %w", err)
	}

	pluginMod := filepath.Join(tmpRoot, "pluginmod")
	if err := os.MkdirAll(pluginMod, 0o750); err != nil {
		return "", fmt.Errorf("mkdir all: %w", err)
	}

	gomod := fmt.Sprintf("module pluginbuild\ngo 1.23\nrequire github.com/prysmaticlabs/protoc-gen-go-cast %s\nrequire google.golang.org/protobuf %s\n", castPin, protobufGoVer)
	if err := os.WriteFile(filepath.Join(pluginMod, "go.mod"), []byte(gomod), 0o600); err != nil {
		return "", fmt.Errorf("writeFile: %w", err)
	}

	fmt.Printf("building protoc-gen-go-cast + protoc-gen-go against protobuf-go %s\n", protobufGoVer)
	env := []string{"GOFLAGS=-mod=mod"}
	if err := shInDir(pluginMod, env, "go", "build", "-o", filepath.Join(binDir, "protoc-gen-go-cast"), "github.com/prysmaticlabs/protoc-gen-go-cast"); err != nil {
		return "", fmt.Errorf("shInDir: %w", err)
	}

	if err := shInDir(pluginMod, env, "go", "build", "-o", filepath.Join(binDir, "protoc-gen-go"), "google.golang.org/protobuf/cmd/protoc-gen-go"); err != nil {
		return "", fmt.Errorf("shInDir: %w", err)
	}

	return binDir, nil
}

// stageProtos copies every proto/**/*.proto into stage/<same path> with the
// mainnet dict substituted.
func stageProtos(stage string) error {
	keys := make([]string, 0, len(mainnetDict))
	for k := range mainnetDict {
		keys = append(keys, k)
	}
	sort.Slice(keys, func(i, j int) bool {
		if len(keys[i]) != len(keys[j]) {
			return len(keys[i]) > len(keys[j])
		}

		return keys[i] < keys[j]
	})

	return filepath.WalkDir("proto", func(path string, d os.DirEntry, err error) error {
		if err != nil || d.IsDir() || !strings.HasSuffix(path, ".proto") {
			return err
		}
		data, err := os.ReadFile(path) // #nosec G304 -- path comes from WalkDir over the repo proto tree
		if err != nil {
			return fmt.Errorf("readFile: %w", err)
		}

		s := string(data)
		for _, k := range keys {
			s = strings.ReplaceAll(s, k, mainnetDict[k])
		}

		dst := filepath.Join(stage, path)
		if err := os.MkdirAll(filepath.Dir(dst), 0o750); err != nil {
			return fmt.Errorf("mkdir all: %w", err)
		}

		if err := os.WriteFile(dst, []byte(s), 0o600); err != nil {
			return fmt.Errorf("writeFile: %w", err)
		}

		return nil
	})
}

// buildMMAP builds protoc's M (import-mapping) option string.
func buildMMAP(stage string) (string, error) {
	var b strings.Builder
	stageProto := filepath.Join(stage, "proto")
	err := filepath.WalkDir(stageProto, func(path string, d os.DirEntry, err error) error {
		if err != nil || d.IsDir() || !strings.HasSuffix(path, ".proto") {
			return err
		}

		rel, err := filepath.Rel(stage, path)
		if err != nil {
			return fmt.Errorf("filepath.Rel: %w", err)
		}

		fmt.Fprintf(&b, ",M%s=github.com/OffchainLabs/prysm/v7/%s", rel, filepath.Dir(rel))

		return nil
	})
	if err != nil {
		return "", err
	}

	b.WriteString(",Mgoogle/api/annotations.proto=google.golang.org/genproto/googleapis/api/annotations")
	b.WriteString(",Mgoogle/api/http.proto=google.golang.org/genproto/googleapis/api/annotations")

	return b.String(), nil
}

// pkgProtos lists the compiled *.proto of a package (sorted, excluding the
// never-compiled ones).
func pkgProtos(dir string) []string {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil
	}

	var out []string
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".proto") {
			continue
		}

		p := filepath.ToSlash(filepath.Join(dir, e.Name()))
		if excludeProtos[p] {
			continue
		}

		out = append(out, p)
	}

	sort.Strings(out)

	return out
}

// normalizeGenerated strips everything before the "Code generated by
// protoc-gen-go" line and normalizes the
// non-semantic "// protoc vX" stamp, in place.
func normalizeGenerated(path string) error {
	data, err := os.ReadFile(path) // #nosec G304 -- path comes from WalkDir over our own temp out dir
	if err != nil {
		return fmt.Errorf("readFile: %w", err)
	}

	lines := strings.SplitAfter(string(data), "\n")
	var b strings.Builder
	started := false
	for _, line := range lines {
		if !started {
			if !strings.HasPrefix(line, codeGenStart) {
				continue
			}

			started = true
		}

		nl := ""
		body := line
		if strings.HasSuffix(line, "\n") {
			body, nl = line[:len(line)-1], "\n"
		}

		body = protocStampRE.ReplaceAllString(body, "$1        "+protocStamp)
		b.WriteString(body + nl)
	}

	return os.WriteFile(path, []byte(b.String()), 0o600)
}
